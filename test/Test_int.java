package test;

/**
 * int primitive test
 */
public class Test_int {

    public static int int_add(int a, int b) {
        return a + b;
    }

    public static int int_sub(int a, int b) {
        return a - b;
    }

    public static int int_mul(int a, int b) {
        return a * b;
    }

    public static int int_div(int a, int b) {
        return a / b;
    }

    public static int int_rem(int a, int b) {
        return a % b;
    }

    public static int int_mix(int a, int b, int c, int d, int e) {
        return (((a + b) - c) * d ) / e;
    }

}
